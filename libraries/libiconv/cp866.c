int	cp866_mbtowc(struct conv_state *cs, const char *in, size_t insize,
	    WIDECHAR *out, size_t *inused);
int	cp866_wctomb(struct conv_state *cs, WIDECHAR in, char *out,
	    size_t outsize, size_t *outused);

/* ARGSUSED */
int
cp866_mbtowc(struct conv_state *cs, const char *in, size_t insize,
    WIDECHAR *out, size_t *inused)
{
	u_char ch;

	ch = *(u_char *)in;
	*out = (ch < 0x80) ? ch : cp8662wc[ch - 0x80];
	*inused = 1;
	return (0);
}

int
cp866_wctomb(struct conv_state *cs, WIDECHAR in, char *out, size_t outsize,
    size_t *outused)
{
	u_char ch;

	if (outsize < 1)
		return (E2BIG);

	if (in < 0x80)
		*out = (char)in;
	else {
		ch = 0;
		if (in >= 0x00a0 && in < 0x00a0 + sizeof(wc2cp866_00))
			ch = wc2cp866_00[in - 0x00a0];
		else if (in >= 0x0401 && in < 0x0401 + sizeof(wc2cp866_04))
			ch = wc2cp866_04[in - 0x0401];
		else if (in == 0x2116)	/* #NUMERO SIGN */
			ch = 0xfc;
		else if (in == 0x2219)	/* #BULLET OPERATOR */
			ch = 0xf9;
		else if (in == 0x221a)	/* #SQUARE ROOT */
			ch = 0xfb;
		else if (in >= 0x2500 && in < 0x2500 + sizeof(wc2cp866_25))
			ch = wc2cp866_25[in - 0x2500];

		if (ch == 0)
			return (EILSEQ);
		*(u_char *)out = ch;
	}
	*outused = 1;
	return (0);
}
