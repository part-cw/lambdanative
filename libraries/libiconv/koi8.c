/* ARGSUSED */
int
koi8r_mbtowc(struct conv_state *cs, const char *in, size_t insize,
    WIDECHAR *out, size_t *inused)
{
	u_char ch;

	ch = *(u_char *)in;
	*out = (ch < 0x80) ? ch : koi8r2wc[ch - 0x80];
	*inused = 1;
	return (0);
}

int
koi8r_wctomb(struct conv_state *cs, WIDECHAR in, char *out, size_t outsize,
    size_t *outused)
{
	u_char ch;

	if (outsize < 1)
		return (E2BIG);

	if (in < 0x80)
		*out = (char)in;
	else {
		ch = 0;
		if (in >= 0x00a0 && in < 0x00a0 + sizeof(wc2koi8r_00))
			ch = wc2koi8r_00[in - 0x00a0];
		else if (in >= 0x0401 && in < 0x0401 + sizeof(wc2koi8r_04))
			ch = wc2koi8r_04[in - 0x0401];
		else if (in >= 0x2219 && in < 0x2219 + sizeof(wc2koi8r_22))
			ch = wc2koi8r_22[in - 0x2219];
		else if (in >= 0x2320 && in < 0x2320 + sizeof(wc2koi8r_23))
			ch = wc2koi8r_23[in - 0x2320];
		else if (in >= 0x2500 && in < 0x2500 + sizeof(wc2koi8r_25))
			ch = wc2koi8r_25[in - 0x2500];

		if (ch == 0)
			return (EILSEQ);
		*(u_char *)out = ch;
	}
	*outused = 1;
	return (0);
}

